   public static String getRandKey(String info) {
       char[] returnChar = new char[18];
       char[] randStr = info.toCharArray();

       int keyLength = 8;
       for (int i = 0; i < keyLength; i++) {
           int randNum = new Random().nextInt(16);
           returnChar[i] = randStr[randNum];
       }
       return String.copyValueOf(returnChar);
   }
   
