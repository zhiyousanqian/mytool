   public static String makePicFileName(String name) {
       try {
           String filenameTmp = name;
           MessageDigest md5 = MessageDigest.getInstance("MD5");
           byte[] b = md5.digest(filenameTmp.getBytes("GBK"));
           StringBuffer sb = new StringBuffer();
           for (byte b1 : b) {
               sb.append(String.format("%02x", b1));
           }
           return sb.toString().trim();
       } catch (Exception e) {
           e.printStackTrace();
       }
       return "";
   }
